package com.XiShuai;

public class MethodDemo {



    public static void main(String[] args) {
        isEvenNumber();
    }


    public static void isEvenNumber() {
        int i = 10;
        if (i % 2 == 0) {
            System.out.println(true);
        } else {
            System.out.println(false);
        }
    }





}


